/**
 * thrash 개입 nudge — 같은 편집 도구를 임계치 이상 반복 호출했을 때
 * AgentSession의 prepareStep이 그 스텝의 system에 덧붙여 주입한다(전략 전환 유도).
 */
export function buildThrashNudge(toolName: string, count: number): string {
  return `## ⚠ 반복 호출 주의 (자동 개입)

\`${toolName}\` 도구를 이미 ${count}번 호출했습니다. **같은 방식을 반복하지 마세요.**
- 먼저 \`read_document\`로 현재 문서 상태를 정확히 다시 확인하세요(변경이 실제로 반영됐는지, 대상 위치·좌표가 맞는지).
- 접근을 바꾸세요: 좌표 모드↔라벨 모드, 다른 도구, 또는 입력 교정. 도구가 오류·미적용을 알렸다면 그 사유를 그대로 따르세요(예: \`.hwp\` 표·셀은 한글에서 .hwpx로 저장 후 편집).
- 그래도 불가능하면 추측해 반복하지 말고, 무엇이 왜 안 되는지 사용자에게 솔직히 설명하고 멈추세요.`;
}

// ─────────────────────────────────────────────────
// 동적 컨텍스트 (매 요청마다 변경 가능)
// ─────────────────────────────────────────────────

function buildDynamicContext(ctx: SystemPromptContext): string {
  const lines: string[] = ["## 현재 컨텍스트"];

  lines.push(`- **작업 디렉터리**: \`${ctx.cwd}\``);

  if (ctx.mcpServers.length > 0) {
    lines.push(`- **연결된 MCP 서버**: ${ctx.mcpServers.join(", ")}`);
  } else {
    lines.push("- **연결된 MCP 서버**: 없음");
  }

  if (ctx.openDocuments.length > 0) {
    // 파일명을 JSON.stringify로 감싸 줄바꿈·마크다운 특수문자로 프롬프트 구조가 오염되지 않도록 한다(M1 신뢰 경계)
    lines.push(
      `- **열람한 문서**:\n${ctx.openDocuments.map((d) => `  - ${JSON.stringify(d)}`).join("\n")}`,
    );
  }

  return lines.join("\n");
}
