/**
 * 시스템 프롬프트 빌더
 * docs/SPEC.md §5 — 안정 prefix 우선(캐시 친화), 동적 컨텍스트 마지막
 * 목표 ~1.5k 토큰
 */

export interface SystemPromptContext {
  cwd: string;
  mcpServers: string[];
  openDocuments: string[];
  /** 등록된 도구 이름 — 능력·한계 섹션의 "가능"을 레지스트리에서 자동 도출(드리프트 방지) */
  toolNames: string[];
}

/**
 * AgentSession에 주입할 시스템 프롬프트를 생성한다.
 *
 * 섹션 구성 (안정 prefix 먼저 — 캐시 친화):
 * 1. 역할
 * 2. 문서 규칙
 * 3. 편집 안전 규칙
 * 4. 법령 규칙
 * 5. 동적 컨텍스트 (마지막)
 */
export function buildSystemPrompt(ctx: SystemPromptContext): string {
  const stable = [
    ROLE_SECTION,
    DOCUMENT_RULES_SECTION,
    EDIT_SAFETY_SECTION,
    buildCapabilitiesSection(ctx.toolNames),
    LAW_RULES_SECTION,
  ].join("\n\n");
  const dynamic = buildDynamicContext(ctx);
  return `${stable}\n\n${dynamic}`;
}

