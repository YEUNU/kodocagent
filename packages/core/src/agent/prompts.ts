/**
 * 능력·한계 섹션 — "가능"은 등록된 도구 이름에서 **자동 도출**(하드코딩 없음 → 드리프트 방지),
 * "불가"는 도구로 표현 불가능한(=어떤 도구도 없는) 알려진 작업 힌트(수동, 안정적).
 * MCP 도구는 세션마다 바뀌므로 stable prefix 캐시를 위해 "가능" 목록에선 제외하고 동적 컨텍스트로 안내.
 */
function buildCapabilitiesSection(toolNames: string[]): string {
  const docTools = toolNames.filter((n) => !n.startsWith("mcp__"));
  const available = docTools.length > 0 ? docTools.map((n) => `\`${n}\``).join(", ") : "(없음)";
  return `## 능력·한계 (현재 도구로 가능한 것과 아닌 것)

가능: **아래 등록된 도구가 너의 능력 전부다**(레지스트리에서 자동 반영) — ${available}. 연결된 MCP 도구가 있으면 함께 사용하세요(아래 컨텍스트 참조).
현재 불가(요청받으면 솔직히 알릴 것): 위 도구로 처리할 수 없는 작업 — 글꼴·굵게·줄간격 등 문자/문단 서식과 제목 스타일 일관화, 머리말/꼬리말·쪽번호 레이아웃/서식, 각주 삽입·형식, 목차·개요번호 자동 갱신, 그림·표 번호 상호참조, 이미지·도형·조직도 편집, 중첩(복합)표 안쪽 셀 지정. **.hwp(한/글 바이너리) 문서의 표·셀 편집**도 불가 — 표·셀을 고치려면 한글에서 '다른 이름으로 저장 → .hwpx'로 변환해야 한다고 안내하세요(.hwp 본문 텍스트 편집은 propose_edit으로 가능).
복잡하거나 여러 작업이 섞인 요청은 먼저 무엇을 어떤 도구로 할지 간단히 정리하고, 위 도구로 안 되는 부분이 있으면 **추측해 어설프게 시도하지 말고 그 사실을 먼저 알린 뒤** 가능한 부분만 진행하거나 대안을 안내하세요. 단순·단일 요청은 곧바로 진행합니다.`;
}

